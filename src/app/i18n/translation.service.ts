export type TranslationKey = keyof typeof translations.et;

@Injectable({
  providedIn: 'root'
})
export class TranslationService {
  readonly supportedLanguages = [
    { code: 'et' as LanguageCode, labelKey: 'language.et' as TranslationKey, flag: '🇪🇪' },
    { code: 'en' as LanguageCode, labelKey: 'language.en' as TranslationKey, flag: '🇬🇧' },
    { code: 'fi' as LanguageCode, labelKey: 'language.fi' as TranslationKey, flag: '🇫🇮' }
  ];

  readonly language = signal<LanguageCode>(this.resolveInitialLanguage());

  constructor() {
    this.applyDocumentLanguage(this.language());
  }

  setLanguage(language: LanguageCode): void {
    this.language.set(language);
    localStorage.setItem(STORAGE_KEY, language);
    this.applyDocumentLanguage(language);
  }

  translate(key: TranslationKey, params?: TranslationParams): string {
    const template = translations[this.language()][key] ?? translations.et[key];
    return this.interpolate(template, params);
  }

  private resolveInitialLanguage(): LanguageCode {
    const storedLanguage = localStorage.getItem(STORAGE_KEY);
    if (storedLanguage === 'et' || storedLanguage === 'en' || storedLanguage === 'fi') {
      return storedLanguage;
    }

    return 'et';
  }

  private applyDocumentLanguage(language: LanguageCode): void {
    document.documentElement.lang = language;
  }

  private interpolate(template: string, params?: TranslationParams): string {
    if (!params) {
      return template;
    }

    return Object.entries(params).reduce(
      (result, [key, value]) => result.replaceAll(`{${key}}`, String(value)),
      template
    );
  }
}
